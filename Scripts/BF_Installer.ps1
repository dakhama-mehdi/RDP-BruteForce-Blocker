$TaskName   = "BruteForceBlocker"
$ScriptName = "BF_Blocker.ps1"
$SourcePath = Join-Path $PSScriptRoot $ScriptName
$TargetPath = "C:\temp\BruteForceBlocker\"
$DestPath   = Join-Path $TargetPath $ScriptName

Write-Host "== BruteForce-Blocker Installation ==" -ForegroundColor Cyan

# Check Admin
if (-not ([Security.Principal.WindowsPrincipal] [Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole([Security.Principal.WindowsBuiltInRole] "Administrator")) {
    Write-Host "Please run this script as Administrator." -ForegroundColor Red
    exit
}

# Create directory 
if (!(Test-Path $TargetPath)) {
    Write-Host "Creating directory $TargetPath"
    New-Item -ItemType Directory -Path $TargetPath -Force | Out-Null
}

# Copy script 
if (!(Test-Path $SourcePath)) {
    Write-Host "ERROR: $ScriptName not found in current folder." -ForegroundColor Red
    exit
}

Write-Host "Copying script to $TargetPath"
Copy-Item $SourcePath $DestPath -Force

# Create Scheduled Task 
Write-Host "Creating scheduled task..."

$xml = @"
<Task version="1.4" xmlns="http://schemas.microsoft.com/windows/2004/02/mit/task">
  <Triggers>
    <EventTrigger>
      <Enabled>true</Enabled>
      <Subscription>
        <![CDATA[
<QueryList>
  <Query Id="0" Path="Security">
    <Select Path="Security">
      *[System[(EventID=4625)]]
      and
      *[EventData[Data[@Name='LogonType']='3']]
    </Select>
  </Query>
</QueryList>
        ]]>
      </Subscription>
    </EventTrigger>
  </Triggers>

  <Principals>
    <Principal id="Author">
      <UserId>SYSTEM</UserId>
      <RunLevel>HighestAvailable</RunLevel>
      <LogonType>InteractiveToken</LogonType>
    </Principal>
  </Principals>

  <Settings>
    <MultipleInstancesPolicy>Queue</MultipleInstancesPolicy>
    <AllowStartOnDemand>true</AllowStartOnDemand>
    <ExecutionTimeLimit>PT1H</ExecutionTimeLimit>
    <StartWhenAvailable>true</StartWhenAvailable>
    <Enabled>true</Enabled>
  </Settings>

  <Actions Context="Author">
    <Exec>
      <Command>powershell.exe</Command>
      <Arguments>-NoProfile -ExecutionPolicy Bypass -File $DestPath</Arguments>
    </Exec>
  </Actions>
</Task>
"@

# Remove old task
if (Get-ScheduledTask -TaskName $TaskName -ErrorAction SilentlyContinue) {
    Unregister-ScheduledTask -TaskName $TaskName -Confirm:$false
}

# Create task
Register-ScheduledTask -TaskName $TaskName -Xml $xml

Write-Host "Scheduled task created successfully!" -ForegroundColor Green

# --- Final message ---
Write-Host ""
Write-Host "Installation complete " -ForegroundColor Green
Write-Host "Task Name: $TaskName"
Write-Host "Script Path: $DestPath"
Write-Host ""
